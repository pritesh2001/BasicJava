package Operators;

public class IncremntDremnt {
    public static void main(String[] args) {
        // increment decrement

        int a = 5;
        System.out.println(a);
        a++;
        System.out.println(a);

        int b = 6;
        System.out.println(b);
        b--;
        System.out.println(b);
    }
}
