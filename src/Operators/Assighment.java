package Operators;

public class Assighment {
    public static void main(String[] args) {
        // assigment

        int a =5;    // a is variable int datatype = is assigment operator 5 is value
        a=10;
        // +=
        a+=10; // a= a+10 (answer is 20 adding)
        System.out.println(a);

        // -=
        a-=5;   // a= a-5  (answer is 15 subtract)
        System.out.println(a);

        //*=
        a*=5;  // a= a*5
        System.out.println(a);

        // /=

        a/=5;   // a= a/5
        System.out.println(a);

        // %=

        a%=7;  // a= a%7
        System.out.println(a);


    }
}
